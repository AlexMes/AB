    /**
     * Determine is marker applicable to the lead
     *
     * @param \App\Lead $lead
     *
     * @return bool
     */
    public function applicableTo(Lead $lead): bool
    {
        if (Str::contains(optional($lead->offer)->name, ['KZ','GAZPROM_SEVER'])) {
            return false;
        }

        if (Str::contains(Str::lower($lead->firstname), $this->falsePositiveNames())) {
            return false;
        }

        return Str::contains(Str::lower($lead->firstname), $this->names())
                || Str::contains(Str::lower($lead->firstname), $this->kyrgizNames())
                || Str::contains(Str::lower($lead->firstname), $this->armenianNames())
                || Str::contains(Str::lower($lead->firstname), $this->tadzhikistantianNames());
    }

    /**
     * Get marker definition
     *
     * @return string
     */
